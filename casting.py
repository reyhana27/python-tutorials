# Casting in Python can help you specify you variable's type

# int() will remove the decimal portion of the number in parenthesis

# str() will set anything put in parenthesis to a string even if it is a number

# float() will set anything in parenthesis to a number that contains decimals

g = int(3.4) # value becomes 3
v = str(5) # 5 becomes a string
b = float(2) # 2 becomes a float instead of an integer

